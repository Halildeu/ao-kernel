"""Canned envelopes for `governed_bugfix` scenario.

Envelope shape follows the `codex-stub` adapter manifest:
`output_parse` rules declare two capabilities (`review_findings` +
`commit_message`), so happy envelopes MUST carry both payload
fields; missing either trips the walker into
`AdapterOutputParseError`.

`gh-cli-pr` adapter has no `output_parse`; its envelope is a
simple `status=ok` + PR ref payload.
"""

from __future__ import annotations

from typing import Any


_BUG_DIFF = (
    "--- a/src/foo.py\n"
    "+++ b/src/foo.py\n"
    "@@ -1 +1 @@\n"
    "-x = 1\n"
    "+x = 2\n"
)


def coding_agent_happy() -> dict[str, Any]:
    """Happy `invoke_coding_agent` envelope — codex-stub with both
    output_parse payloads (review_findings empty list + stub
    commit_message) plus the diff + cost."""
    return {
        "status": "ok",
        "diff": _BUG_DIFF,
        "review_findings": {
            "schema_version": "1",
            "findings": [],
            "summary": "Bug fix carries no review artefacts.",
        },
        "commit_message": {
            "schema_version": "1",
            "subject": "Apply governed bugfix",
            "body": "Auto-generated by codex-stub for governed_bugfix benchmark.",
        },
        "cost_actual": {
            "tokens_input": 120,
            "tokens_output": 45,
            "time_seconds": 2.3,
        },
    }


def open_pr_happy() -> dict[str, Any]:
    """Happy `open_pr` envelope — gh-cli-pr has no output_parse so
    no extracted payloads are required; just a minimal PR ref."""
    return {
        "status": "ok",
        "pr_url": "https://github.example/ao-kernel/pull/999",
        "pr_number": 999,
        "cost_actual": {"time_seconds": 0.4},
    }


__all__ = ["coding_agent_happy", "open_pr_happy"]
